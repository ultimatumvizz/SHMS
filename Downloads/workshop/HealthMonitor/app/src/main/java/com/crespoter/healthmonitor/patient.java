package com.crespoter.healthmonitor;


public class patient {
    String name;
    String age;
    String sex;
    String temperature;
    String pulse;
    String id;
    public patient(String name,String age,String sex,String temperature,String pulse,String id)
    {
        this.name = name;
        this.age = age;
        this.sex = sex;
        this.temperature = temperature;
        this.pulse = pulse;
        this.id = id;
    }
}
